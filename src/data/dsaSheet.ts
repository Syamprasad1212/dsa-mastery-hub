// Calculate totals
export const getTotalProblems = (): number => {
  return dsaSheet.reduce((total, phase) => 
    total + phase.topics.reduce((topicTotal, topic) => 
      topicTotal + topic.problems.length, 0), 0);
};

export const getDifficultyCount = (): { easy: number; medium: number; hard: number } => {
  const counts = { easy: 0, medium: 0, hard: 0 };
  dsaSheet.forEach(phase => {
    phase.topics.forEach(topic => {
      topic.problems.forEach(problem => {
        if (problem.difficulty === 'Easy') counts.easy++;
        else if (problem.difficulty === 'Medium') counts.medium++;
        else counts.hard++;
      });
    });
  });
  return counts;
};
